package weng.feature;

public class Predication {
    private double ab;
    private double bc;

    public Predication(double ab, double bc) {
        this.ab = ab;
        this.bc = bc;
    }

    public void setAb(double ab) {
        this.ab = ab;
    }

    public void setBc(double bc) {
        this.bc = bc;
    }

    public double getAb() {
        return ab;
    }

    public double getBc() {
        return bc;
    }
}
